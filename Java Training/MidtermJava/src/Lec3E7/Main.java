package Lec3E7;


public class Main {
    public static void main(String[] args) {
        System.out.println("square root of 37 is "+Math.sqrt(37));
        System.out.println("the sine of 300 is "+Math.sin(300));
        System.out.println("the cosine of 300 is "+Math.cos(300));
        System.out.println("floor of 22.8 is "+Math.floor(22.8));
        System.out.println("ceil of 22.8 is "+Math.ceil(22.8));
        System.out.println("round of 22.8 is "+Math.round(22.8));
        System.out.println("\"D\" vs 71. winner is "+Math.max('D', 71));
        System.out.println("Random num 0-20 is "+Math.random()*100%20);
    }
}
